CREATE TABLE "BANK_USER_INFO" (
   "USER_ID"   VARCHAR2(255)      PRIMARY KEY,
   "EMAIL"   VARCHAR2(255)      NOT NULL,
   "PASSWORD"   VARCHAR2(255)      NULL,
   "NAME"   VARCHAR2(255)      NULL,
   "BIRTH_DATE"   DATE      NULL,
   "PHONE_NUMBER"   VARCHAR2(20)      NULL,
   "ADDR"   VARCHAR2(255)      NULL,
   "ACCOUNT_LINK"   VARCHAR2(255)      NULL,
   "ADMIN_RIGHT"   NUMBER(1)      NULL
);

CREATE TABLE "SAVING_ACCOUNT" (
   "BANK_CD"   VARCHAR2(255)      NOT NULL,
   "AC_NUMBER"   VARCHAR2(255)      PRIMARY KEY,
   "USER_ID"   VARCHAR2(255)      NOT NULL,
   "AC_PW"   VARCHAR2(4)      NULL,
   "AC_NAME"   VARCHAR2(255)      NULL,
   "BALANCE"   NUMBER(20)      NULL,
   "RATE"   NUMBER(5)      NULL,
   "AC_OP_DATE"   TIMESTAMP DEFAULT CURRENT_TIMESTAMP      NULL,
   "USER_MEMO"   VARCHAR2(30)      NULL,
   "STATE"   NUMBER(1)      NULL
);

