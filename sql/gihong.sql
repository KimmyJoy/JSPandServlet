   "RATE"   NUMBER(5)      NULL,
   "AC_OP_DATE"   TIMESTAMP DEFAULT CURRENT_TIMESTAMP      NULL,
   "AC_ED_DATE"   DATE      NULL,
    "TR_AC_NUMBER" VARCHAR2(20) NULL,
    "TR_TYPE" VARCHAR2(255) NULL,
    "TR_PAYMENT" NUMBER(20) NULL,
